#!/usr/bin/env node
/**
 * Write android/local.properties (gitignored) with sdk.dir from ANDROID_HOME / ANDROID_SDK_ROOT
 * or the default Android Studio path on Windows/macOS/Linux.
 */
import fs from 'fs';
import os from 'os';
import path from 'path';
import { fileURLToPath } from 'url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const androidDir = path.join(root, 'android');
const outFile = path.join(androidDir, 'local.properties');

function defaultSdkPath() {
  const home = os.homedir();
  if (process.platform === 'win32') {
    return path.join(process.env.LOCALAPPDATA || path.join(home, 'AppData', 'Local'), 'Android', 'Sdk');
  }
  if (process.platform === 'darwin') {
    return path.join(home, 'Library', 'Android', 'sdk');
  }
  return path.join(home, 'Android', 'Sdk');
}

function resolveSdkDir() {
  const fromEnv = process.env.ANDROID_HOME || process.env.ANDROID_SDK_ROOT;
  if (fromEnv && fs.existsSync(fromEnv)) {
    return path.resolve(fromEnv);
  }
  const fallback = defaultSdkPath();
  if (fs.existsSync(fallback)) {
    return fallback;
  }
  return null;
}

function toGradleSdkDir(sdkDir) {
  return sdkDir.replace(/\\/g, '/').replace(/:/g, '\\:');
}

const sdkDir = resolveSdkDir();
if (!sdkDir) {
  console.error(
    'Android SDK not found. Install Android Studio or set ANDROID_HOME, then re-run.',
  );
  process.exit(1);
}

const content = `## Generated by scripts/ensure-android-local-properties.mjs — do not commit\nsdk.dir=${toGradleSdkDir(sdkDir)}\n`;
fs.mkdirSync(androidDir, { recursive: true });
fs.writeFileSync(outFile, content);
console.log(`Wrote ${path.relative(root, outFile)} → ${sdkDir}`);
